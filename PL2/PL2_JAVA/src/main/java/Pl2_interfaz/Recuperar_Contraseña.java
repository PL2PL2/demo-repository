    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String correo = jFormattedTextField1.getText();
        String password = new String(jPasswordField1.getPassword());
        boolean existe = false;
        Cliente necesitado = new Cliente("","","","","","", false);
        File archivo= new File("registroClientes.dat");
        
        if(archivo.exists() && archivo.length() > 0){
            try (ObjectInputStream ois = new ObjectInputStream(new FileInputStream(archivo))) {
                while (true) {
                    Cliente existente = (Cliente) ois.readObject();
                    if (existente.getCorreo_electronico().equals(correo)) {
                        existe = true;
                        necesitado = existente;
                        break;
                    }
                }
            } catch (EOFException eof) {
                // fin del archivo
            } catch (Exception e) {
                e.printStackTrace();
            }
        }
        
        if(existe && necesitado.getClave().equals(password)){
            JOptionPane.showMessageDialog(this, "Contraseña ya utilizada", "Error", JOptionPane.ERROR_MESSAGE);
        }else if(!existe){
            JOptionPane.showMessageDialog(this, "Su correo no está registrado", "Error", JOptionPane.ERROR_MESSAGE);
        }else{
                String nombre = necesitado.getNombre();
                String eMail = necesitado.getCorreo_electronico();
                String clave = password;
                String telefono = necesitado.getTeléfono();
                String direccion = necesitado.getDirección();
                String tarjeta_credito = necesitado.getTarjeta_de_credito();
                boolean VIP = necesitado.isVIP();
            try {   
                    Cliente nuevoCliente = new Cliente(nombre, eMail, clave, telefono, direccion, tarjeta_credito, VIP);
                    FileOutputStream fos = new FileOutputStream(archivo, true);
                    ObjectOutputStream oos;
                    if (archivo.length() == 0) {
                        oos = new ObjectOutputStream(fos); // Escribe encabezado
                    } else {
                        oos = new MiObjectOutputStream(fos); // clase que no escribe encabezado
                    }

                    oos.writeObject(nuevoCliente);
                    oos.close();
                    
                    Login_User user = new Login_User();
                    user.setVisible(true);
                    this.dispose();
                  } catch (IOException ioe) {
                    ioe.printStackTrace();
                    JOptionPane.showMessageDialog(this, "Error al guardar el cliente.", "Error", JOptionPane.ERROR_MESSAGE);
                }
        }
       
    }//GEN-LAST:event_jButton1ActionPerformed
    

